from django.contrib import admin
from django.urls import path, include
from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
)

# Imports for Swagger endpoint documentation
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi

# Swagger schema configuration
schema_view = get_schema_view(
   openapi.Info(
      title="Job Board API",
      default_version='v1',
      description="Jameson API documentation for the Job Board platform backend",
      contact=openapi.Contact(email="jamesonpillay@gmail.com"),
   ),
   public=True,
   permission_classes=[permissions.AllowAny],
   url="https://alx-project-nexus-production-6c5b.up.railway.app",
)

SWAGGER_SETTINGS = {
    'USE_SESSION_AUTH': False,
    'SECURITY_DEFINITIONS': {
        'Token': {
            'type': 'apiKey',
            'name': 'Authorization',
            'in': 'header'
        }
    },
}

urlpatterns = [
    path("admin/", admin.site.urls),
    path("api/token/", TokenObtainPairView.as_view(), name="token_obtain_pair"),
    path("api/token/refresh/", TokenRefreshView.as_view(), name="token_refresh"),

    # jobs, categories and applications API's
    path('', include('jobs.urls')),
    path('api/jobs/', include('jobs.urls')),
    path('api/categories/', include('categories.urls')),
    path('api/applications/', include('applications.urls')),

     # Swagger Docs
    path('api/docs/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),

]
